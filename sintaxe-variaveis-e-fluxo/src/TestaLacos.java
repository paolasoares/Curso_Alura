
public class TestaLacos {
	public static void main(String[] args) {
		for (int multiplicador = 1; multiplicador <= 10; multiplicador++) {
			for (int contado = 0; contado <= 10; contado++) {
				System.out.print(multiplicador * contado);
				System.out.print(" ");
			}
			System.out.println();
		}
	}

}
